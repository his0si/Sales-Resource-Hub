-- ============================================================
-- employees (조직 마스터) 테이블
--   부문/부서(팀)/사번/성명. 세일즈 메모 작성자(author_emp_no, author_name)를
--   이 마스터와 대조해 "메모 원문이 어느 부서/부문인지" 판별하는 데 쓴다.
--   매칭 우선순위: 사번(emp_no) → 성명(name).  (사번은 leading zero 가 있어 문자열)
--   실행: PGPASSWORD=<pw> psql -h 127.0.0.1 -p 5433 -U hansolax -d hansolax -f db/employees.sql
-- ============================================================

CREATE TABLE IF NOT EXISTS employees (
    emp_no    VARCHAR(20)  PRIMARY KEY,   -- 사번 (leading zero 보존 위해 문자열)
    name      VARCHAR(100) NOT NULL,      -- 성명
    team      VARCHAR(100) NOT NULL,      -- 부서(팀)
    division  VARCHAR(100) NOT NULL       -- 부문
);

-- 성명으로 폴백 매칭할 때 쓰는 인덱스
CREATE INDEX IF NOT EXISTS idx_employees_name ON employees (name);

